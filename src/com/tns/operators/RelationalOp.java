package com.tns.operators;
//Code for relational operator

public class RelationalOp {

	public static void main(String[] args) {
		int x=8,y=9;
		
		System.out.println("x=y  "+(x==y));
		System.out.println("x!=y "+(x!=y));
		System.out.println("x>y  "+(x>y));
		System.out.println("x<y  "+(x<y));
		System.out.println("x>=y "+(x>=y));
		System.out.println("x<=y  "+(x<=y));

	}

}
