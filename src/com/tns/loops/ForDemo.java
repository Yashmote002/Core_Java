package com.tns.loops;
//Code for For Loop

public class ForDemo {

	public static void main(String[] args) {
		System.out.println("For Increment");
		for(int i=0;i<=10;i++) {
			System.out.println(i);
		}
		
		System.out.println("For Decrement");
		for(int j=10;j>=5;j--) {
			System.out.println(j);
		}

	}

}
